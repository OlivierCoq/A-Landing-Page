       <script type="text/javascript">     
 
           function btmSubmit() {
                    /*Form Inputs in Variable. Always Keep variables IN SCOPE in function*/
               var letters = /^[A-Za-z]+$/; /*Regex for letters only all the way to end of string*/
			   var emailRegex = /^[a-zA-Z0-9.!#$%&’*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$/;/*Regex (HTML5)*/
               var first = document.getElementById('btm-first').value;
               var last = document.getElementById('btm-last').value;
			   var email = document.getElementById('btm-email').value;
               var phoneReq = document.getElementById('btm-phone').value;
               var themessage = document.getElementById('btm-message').value;

			   
			   if (letters.test(first) == false) {
				  	$('#btm-vdtn-first').fadeIn(500);
                  	$('#btm-vdtn-first').addClass('invalid');
               	} else if (letters.test(last) == false) {
					$('#btm-vdtn-last').fadeIn(500);
					$('#btm-vdtn-last').addClass('invalid');
				} else if (emailRegex.test(email) == false) {
					$('#btm-vdtn-email').fadeIn(500);
                  	$('#btm-vdtn-email').addClass('invalid');
				} else if (phoneReq.length < 9) {
					$('#btm-vdtn-phone').fadeIn(500);
                  	$('#btm-vdtn-phone').addClass('invalid');
				} else if (themessage.length < 2) {
					$('#btm-vdtn-mssg').fadeIn(500);
                  	$('#btm-vdtn-mssg').addClass('invalid');
				} else {
					document.getElementById('btm-form').action = 'http://www.detoxlandingpage.org/includes/btm-process.php';
                    document.getElementById('btm-form').submit();   
				};
            };
